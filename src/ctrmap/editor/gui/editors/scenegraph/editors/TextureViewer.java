	@Override
	public void load(Object o) {
		texture = null;
		if (o != null && o instanceof Texture) {
			Texture t = (Texture)o;
			name.setText(t.name);
			format.setText(t.format.name());
			dim.setText(t.width + "x" + t.height);
			texturePreview1.showTexture(t);
			texture = t;
		}
	}

