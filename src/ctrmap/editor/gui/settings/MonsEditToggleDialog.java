	@Override
	public void setVisible(boolean bln) {
		enableButtonTextQueue = new LinkedList<>(enableButtonTextQueueSource);

		SwingWorkerUtils.executeJob(() -> {
			while (!enableButtonTextQueue.isEmpty()) {
				try {
					Thread.sleep(1000);
					btnConfirm.setText(enableButtonTextQueue.remove());
				} catch (InterruptedException ex) {
					Logger.getLogger(MonsEditToggleDialog.class.getName()).log(Level.SEVERE, null, ex);
				}
				Thread.yield();
			}
			btnConfirm.setEnabled(true);
		});

		super.setVisible(bln);
	}

	public boolean getResult() {
		return result;
	}

