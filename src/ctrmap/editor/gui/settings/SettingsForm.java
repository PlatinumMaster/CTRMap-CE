	public SettingsForm(CTRMap ctrmap) {
		GLOBAL_SETTINGS_PANEL_LOCK = true;
		initComponents();
		panels.add(gfxSettings);
		panels.add(audioSettings);
		panels.add(editorSettings);
		panels.addAll(ctrmap.getEditorManager().getSettingsPanels());
		cm = ctrmap;
		((DefaultCaret) descArea.getCaret()).setUpdatePolicy(DefaultCaret.NEVER_UPDATE);

		setSubformAttachment(this);
	}

	private void setSubformAttachment(Component c) {
		if (c instanceof SettingsPanel) {
			((SettingsPanel) c).attachParent(this);
		}
		if (c instanceof Container){
			for (Component sub : ((Container) c).getComponents()){
				setSubformAttachment(sub);
			}
		}
	}

	public void showHint(SettingsPanel pnl, String resId) {
		descArea.setText(pnl.getMsgTxt().getLineForName(resId));
	}

