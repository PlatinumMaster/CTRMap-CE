    private void btnEnableMonsEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnableMonsEditActionPerformed
		boolean setMonsEditFlag = false;
		CTRMapProject p = parent.cm.getProject();
		boolean v = btnEnableMonsEdit.isSelected();
		if (v) {
			if (parent.cm.getProject().getFlag(PRJFLAG_IS_UNSAFE_MODE)) {
				setMonsEditFlag = true;
			} else {
				MonsEditToggleDialog dlg = new MonsEditToggleDialog(parent, true);
				dlg.setVisible(true);
				if (dlg.getResult()) {
					setMonsEditFlag = true;
					parent.cm.getProject().setFlag(PRJFLAG_IS_UNSAFE_MODE, true);
				} else {
					btnEnableMonsEdit.setSelected(false);
				}
			}
		} else {
			setMonsEditFlag = true;
		}
		if (setMonsEditFlag) {
			parent.cm.broadcastGlobalEvent(CTRMap.GEVENT_FLAG_CHANGE_ID, PRJFLAG_IS_MONSEDIT_ENABLE, v);
			p.setFlag(PRJFLAG_IS_MONSEDIT_ENABLE, v);
			p.saveProjectData();
		}
    }//GEN-LAST:event_btnEnableMonsEditActionPerformed


