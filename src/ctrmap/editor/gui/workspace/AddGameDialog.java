    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
		String gamePath = pathField.getText();
		FSFile gameRoot = new DiskFile(gamePath);
		if (gameRoot.exists()) {
			for (CanAddGameVerifier v : verifiers) {
				if (!v.verifyGamePath(gamePath)) {
					return;
				}
			}
			GameInfo.Game game = GameDetector.detectGameType(gameRoot);
			if (game == null) {
				JOptionPane.showMessageDialog(this, "The given path is not a recognizable Pok√©mon game dump", "Invalid path", JOptionPane.ERROR_MESSAGE);
			} else {
				for (OnGameAddSuccessListener l : listeners) {
					l.onGameAdded(gamePath);
				}
				dispose();
			}
		} else {
			JOptionPane.showMessageDialog(this, "The given path does not exist", "Invalid path", JOptionPane.ERROR_MESSAGE);
		}
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
		String path = pathField.getText();
		FSFile select = XFileDialog.openDirectoryDialog("Select a game dump location", (path == null || path.trim().isEmpty()) ? null : new DiskFile(pathField.getText()));
		if (select != null) {
			pathField.setText(select.getPath());
		}
    }//GEN-LAST:event_btnBrowseActionPerformed


